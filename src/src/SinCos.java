public class SinCos {
    double value;

    public void Sin(double value){
        System.out.println(Math.sin(value));

    }
    public void Cos(double value){
        System.out.println(Math.cos(value));

    }
    public void Tan(double value){
        System.out.println(Math.tan(value));

    }
    public void CoTang(double value){
        System.out.println(1/(Math.tan(value)));

    }

}
